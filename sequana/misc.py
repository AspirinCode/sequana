"""misc utilities"""


def wget(link, output):
    """Retrieve a file from internet.

    :param str link: a valid URL
    :param str output: the output filename

    .. warning:: no sanity check of any kind for now
    """
    import urllib
    urllib.urlretrieve(link, filename=output)
